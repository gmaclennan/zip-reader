if (CP437_CHARS.length !== 256) {
  throw new Error("Invalid CP437 character set length");
}

export function decodeCp437(data: Uint8Array, offset: number = 0): string {
  let str = "";
  for (let i = offset; i < data.length; i++) {
    str += CP437_CHARS[data[i]] ?? "?";
  }
  return str;
}
